-- Create table for storing topic translations
CREATE TABLE topic_translations (
    id BIGSERIAL PRIMARY KEY,
    topic_id BIGINT NOT NULL REFERENCES topics(id) ON DELETE CASCADE,
    language_code VARCHAR(5) NOT NULL,
    name VARCHAR(200) NOT NULL,
    description TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (topic_id, language_code)
);

-- Add indexes for performance
CREATE INDEX idx_topic_translations_topic_id ON topic_translations(topic_id);
CREATE INDEX idx_topic_translations_lang ON topic_translations(language_code);

